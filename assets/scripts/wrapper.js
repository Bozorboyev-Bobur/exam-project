    nameTitle.classList.add('course-card-name-title')
    nameTitle.classList.add('d-flex')
    nameTitle.classList.add('align-self-center')
    const nameTitleText = document.createElement('h3');
    nameTitleText.textContent = name;
    nameTitle.append(nameTitleText);

    filterName.append(nameType, nameTitle);

    const filterLogo = document.createElement('div');
    filterLogo.classList.add('course-card__logo')
    filterLogo.classList.add('align-self-center')
