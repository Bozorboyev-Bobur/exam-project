    submitBtn.append(buttonSubmit)
    

    filterContent.append(filterName, filterLogo, submitBtn)

    const filterInfo = document.createElement('div');
    filterInfo.classList.add('course-card__info');
    filterInfo.classList.add('d-flex');
    filterInfo.classList.add('justify-content-between');

